package com.yx.sz.laboratory.proStandardLib.bean;

public class FoodStandard {
	private int id;
	private int sequence;
	private String name;
	private String category;
	private String standardCode;
	private String standardName;
	private String limitAndDesc;
	private String remark;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getSequence() {
		return sequence;
	}
	public void setSequence(int sequence) {
		this.sequence = sequence;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public String getStandardCode() {
		return standardCode;
	}
	public void setStandardCode(String standardCode) {
		this.standardCode = standardCode;
	}
	public String getStandardName() {
		return standardName;
	}
	public void setStandardName(String standardName) {
		this.standardName = standardName;
	}
	public String getLimitAndDesc() {
		return limitAndDesc;
	}
	public void setLimitAndDesc(String limitAndDesc) {
		this.limitAndDesc = limitAndDesc;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
}
